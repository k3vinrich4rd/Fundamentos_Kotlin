package fundamentos.variaveis

fun main() {
    //Variável imutável (formas de declaração)
    val varName: String = "Kevin"
    val secondName: String = "Reinaldo"
    println(secondName)

    //Variáveis mutáveis (formas de declaração)
    val name = "Kevin"
    println(name)

    val name2 = "Ketelyn"
    println(name2)
}